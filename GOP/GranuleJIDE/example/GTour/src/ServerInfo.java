
public class ServerInfo {
	private String voice_url;
	private String video_url;
	private String server_name;
	private int server_port;
	private String net_style;
	public String getVoiceUrl() {
		return voice_url;
	}
	public void setVoiceUrl(String voice_ur) {
		voice_url = voice_ur;
	}
	public String getVideoUrl() {
		return video_url;
	}
	public void setVideoUrl(String video_ur) {
		video_url = video_ur;
	}
	public String getServerName() {
		return server_name;
	}
	public void setServerName(String server_nam) {
		server_name = server_nam;
	}
	public int getServerPort() {
		return server_port;
	}
	public void setServerPort(int server_por) {
		server_port = server_por;
	}
	public String getNetStyle() {
		return net_style;
	}
	public void setNetStyle(String net_styl) {
		net_style = net_styl;
	}
	
}
